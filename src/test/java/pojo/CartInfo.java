package pojo;

public class CartInfo {
	
	private String processingMTN;
	private String processStep;
	private String intendType;
	private boolean editPlanClick;
	private String	addressLine1;
	private String city;
	private String state;
	private int zipCode;
	private String customerType;
	private String mtnFlow;
	private String processAction;
	
	public String getProcessingMTN() {
		return processingMTN;
	}
	public void setProcessingMTN(String processingMTN) {
		this.processingMTN = processingMTN;
	}
	public String getProcessStep() {
		return processStep;
	}
	public void setProcessStep(String processStep) {
		this.processStep = processStep;
	}
	public String getIntendType() {
		return intendType;
	}
	public void setIntendType(String intendType) {
		this.intendType = intendType;
	}
	public boolean isEditPlanClick() {
		return editPlanClick;
	}
	public void setEditPlanClick(boolean editPlanClick) {
		this.editPlanClick = editPlanClick;
	}
	public String getAddressLine1() {
		return addressLine1;
	}
	public void setAddressLine1(String addressLine1) {
		this.addressLine1 = addressLine1;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public int getZipCode() {
		return zipCode;
	}
	public void setZipCode(int zipCode) {
		this.zipCode = zipCode;
	}
	public String getCustomerType() {
		return customerType;
	}
	public void setCustomerType(String customerType) {
		this.customerType = customerType;
	}
	public String getMtnFlow() {
		return mtnFlow;
	}
	public void setMtnFlow(String mtnFlow) {
		this.mtnFlow = mtnFlow;
	}
	public String getProcessAction() {
		return processAction;
	}
	public void setProcessAction(String processAction) {
		this.processAction = processAction;
	}
	

}
